import * as React from "react"

import { cn } from "@/lib/utils"

/**
 * Card component - A container with rounded borders, background, and shadow.
 * Used as the main container for content sections throughout the application.
 */
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border bg-card text-card-foreground shadow-sm",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

